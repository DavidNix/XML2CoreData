- (void)viewDidLoad
{
    [super viewDidLoad];

    notesArray = [NSMutableArray array];
    
    NSSet *charactersSet = [self.selectedCharacter valueForKey:@"characterNotes"];
    notesArray = (NSMutableArray*)[charactersSet allObjects];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [notesArray count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"notesCell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    NSManagedObject *note = [notesArray objectAtIndex:indexPath.row];
    cell.textLabel.text = [note valueForKey:@"noteDescription"];
    
    return cell;
}

